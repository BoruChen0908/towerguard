"""
Scenario-results assembler — produces the single JSON contract that the frontend
(KT) consumes. This is the one interface between the model half and the
presentation half (see WORK_BORU.md / WORK_KT.md).

It wires together every model module: N2 scenarios + timing, N3 bands, N4 costs,
N5 safety, N8 policy brief, plus a one-at-a-time sensitivity (N11 data) and an
assumptions ledger (N12 data). Nothing new is modelled here — pure assembly +
JSON serialisation.

================================================================================
PROVENANCE & DECISIONS — JSON contract
================================================================================
  - Per-year arrays (series, bands, annual cost, risk) all span FY2025-FY2036
    and are index-aligned with `years`, so the frontend can zip them directly.
  - Cumulative cost figures EXCLUDE the FY2025 base year (projected FY2026-2036),
    matching N4's convention; noted in meta.notes.
  - Sensitivity target metric = the do-nothing FY2036 workforce (avoids the
    loop/cost re-pricing coupling); shows which assumptions move the outcome most.
  - The example file is committed so KT can build against real data offline; it
    is regenerated by `python -m models.scenario_results`.

OBSERVED BEHAVIOR (model output, not input)
  The assembled JSON is ~23KB: 7 sections + safety_context, 5 scenarios with
  12-length index-aligned per-year arrays, timing for 5 start years, 5
  sensitivity params, 12 assumptions, and the policy brief. Matches the
  WORK_BORU.md contract sketch field-for-field.
================================================================================
"""

